class TaskForm(FlaskForm):
    title = StringField('Title', validators=[DataRequired()])
    body = TextAreaField('Description', validators=[DataRequired()])
    status = SelectField(u'Status', choices=[('open', 'open'), ('in test', 'in test'), ('done', 'done'), ('in process', 'in process')])
    performer = StringField('Choose performer', validators=[DataRequired()])
    submit = SubmitField('Submit')
